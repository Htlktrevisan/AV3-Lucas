//Crie um programa no qual o usuário deve digitar um número (base) e o seu expoente. Apresentar na tela o resultado da exponenciação.

//variaveis 
let base 
let expoente 
let resultadoexponeciação 

alert("Programa feito para digitar numero de bases e expoente e mostrar o resultado:")

//Entrada de Dados 
base = Number(prompt("Qual é o valor da base: "))
expoente = Number(prompt("qual é o valor do expoente: "))

//Calculos 
resultadoexponeciação = base ** expoente

//Saida de Dados
alert("O resultado exponecia é igual:"+resultadoexponeciação+"")

